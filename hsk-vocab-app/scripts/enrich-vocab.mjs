// ─── Topic Category Assignment ───────────────────────────────────────────────
function assignTopic(english, pos) {
  const lower = english.toLowerCase();
  for (const rule of topicRules) {
    for (const kw of rule.keywords) {
      // Match whole word or word boundary
      if (lower.includes(kw)) {
        // Extra check: for short keywords, ensure word boundary
        if (kw.length <= 3) {
          const re = new RegExp('\\b' + kw.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '\\b', 'i');
          if (re.test(lower)) return rule.category;
        } else {
          return rule.category;
        }
      }
    }
  }
  return 'general';
}

// ─── Example Sentence Generation ─────────────────────────────────────────────
function generateExamples(chinese, pos) {
  const posList = Array.isArray(pos) ? pos : [];

  if (posList.includes('verb')) {
    return [`我${chinese}了。`, `他正在${chinese}。`, `你${chinese}什么？`];
  }
  if (posList.includes('noun')) {
    return [`这是我的${chinese}。`, `他的${chinese}很好。`, `我喜欢这个${chinese}。`];
  }
  if (posList.includes('adjective')) {
    return [`今天很${chinese}。`, `这是一个${chinese}的地方。`, `她非常${chinese}。`];
  }
  if (posList.includes('adverb')) {
    return [`我${chinese}喜欢。`, `他${chinese}去了。`, `她${chinese}很高兴。`];
  }
  if (posList.includes('number')) {
    return [`我有${chinese}个朋友。`, `${chinese}个人来了。`];
  }
  if (posList.includes('pronoun')) {
    return [`${chinese}是学生。`, `${chinese}喜欢看电影。`];
  }
  if (posList.includes('particle')) {
    return [`好${chinese}！`, `是${chinese}，我知道了。`];
  }
  if (posList.includes('measure')) {
    return [`一${chinese}书。`, `两${chinese}猫。`];
  }
  if (posList.includes('preposition')) {
    return [`我${chinese}朋友一起去。`, `他${chinese}我在一起很开心。`];
  }
  if (posList.includes('conjunction')) {
    return [`我${chinese}他都是学生。`, `你喜欢茶${chinese}咖啡？`];
  }
  if (posList.includes('interjection')) {
    return [`${chinese}，你说什么？`, `${chinese}，太好了！`];
  }
  if (posList.includes('prefix') || posList.includes('suffix')) {
    return [`这个${chinese}很有意思。`, `那个${chinese}不太好。`];
  }
  return [`我知道${chinese}的意思。`, `请再说一次${chinese}。`];
}

// ─── Radical & Stroke Count Lookup ───────────────────────────────────────────
function lookupCharInfo(chinese) {
  const firstChar = chinese.charAt(0);
  const info = charLookup[firstChar];
  if (info) {
    return { radical: info[0], stroke_count: info[1] };
  }
  return { radical: '', stroke_count: 0 };
}

// ─── Main Processing ─────────────────────────────────────────────────────────
const levelFiles = [
  'hsk_level_1.json',
  'hsk_level_2.json',
  'hsk_level_3.json',
  'hsk_level_4.json',
];

let allWords = [];

for (const file of levelFiles) {
  const filePath = resolve(publicDir, file);
  console.log(`Processing ${file}...`);
  const raw = readFileSync(filePath, 'utf-8');
  const data = JSON.parse(raw);
  let count = 0;

  data.words = data.words.map((word) => {
    const topic = assignTopic(word.english, word.pos);
    const examples = generateExamples(word.chinese, word.pos);
    const charInfo = lookupCharInfo(word.chinese);

    count++;
    return {
      ...word,
      topic_category: topic,
      example_sentences: examples,
      radical: charInfo.radical,
      stroke_count: charInfo.stroke_count,
    };
  });

  writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
  console.log(`  Enriched ${count} words in ${file}`);
  allWords = allWords.concat(data.words);
}

// Write combined file
const combinedPath = resolve(publicDir, 'hsk_vocabulary_complete.json');
const combined = { words: allWords };
writeFileSync(combinedPath, JSON.stringify(combined, null, 2) + '\n', 'utf-8');
console.log(`\nWrote combined file with ${allWords.length} words to hsk_vocabulary_complete.json`);

// Print category stats
const stats = {};
for (const w of allWords) {
  stats[w.topic_category] = (stats[w.topic_category] || 0) + 1;
}
console.log('\nTopic category distribution:');
for (const [cat, cnt] of Object.entries(stats).sort((a, b) => b[1] - a[1])) {
  console.log(`  ${cat}: ${cnt}`);
}
